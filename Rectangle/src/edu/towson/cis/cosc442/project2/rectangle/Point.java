package edu.towson.cis.cosc442.project2.rectangle;

/**
 * The Point Class.
 */
public class Point {
	
	/** x and y coordinates. */
	public Double x, y;
	
	/**
	 * Instantiates a new point.
	 *
	 * @param xValue the x
	 * @param yValue the y
	 */
	Point(Double xValue, Double yValue) {
		this.x = xValue;
		this.y = yValue;
	}
}
